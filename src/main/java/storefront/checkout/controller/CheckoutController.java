package storefront.checkout.controller;

import jakarta.validation.Valid;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;
import storefront.checkout.dto.Checkout;
import storefront.checkout.service.CheckoutService;

@RestController
@RequestMapping("/checkout")
public class CheckoutController {
    private final CheckoutService checkoutService;

    public CheckoutController(CheckoutService checkoutService) {
        this.checkoutService = checkoutService;
    }

    @PostMapping
    public ResponseEntity<Integer> checkout(@Valid @RequestBody Checkout checkout) {
        return new ResponseEntity<>(checkoutService.checkoutProduct(checkout), HttpStatus.OK);
    }
}